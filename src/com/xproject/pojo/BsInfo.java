package com.xproject.pojo;

public class BsInfo {
	private int id;
	private int frequency;
	private int rssi;
	private int mcc;
	private int mnc;
	private int la;
	private String areas_id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getFrequency() {
		return frequency;
	}
	public void setFrequency(int frequency) {
		this.frequency = frequency;
	}
	public int getRssi() {
		return rssi;
	}
	public void setRssi(int rssi) {
		this.rssi = rssi;
	}
	public int getMcc() {
		return mcc;
	}
	public void setMcc(int mcc) {
		this.mcc = mcc;
	}
	public int getMnc() {
		return mnc;
	}
	public void setMnc(int mnc) {
		this.mnc = mnc;
	}
	public int getLa() {
		return la;
	}
	public void setLa(int la) {
		this.la = la;
	}
	public String getAreas_id() {
		return areas_id;
	}
	public void setAreas_id(String areas_id) {
		this.areas_id = areas_id;
	}


}
